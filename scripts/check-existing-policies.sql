-- Check existing RLS policies for sharing-related tables

-- List all policies on permissions table
SELECT 
    schemaname,
    tablename,
    policyname,
    permissive,
    roles,
    cmd,
    qual,
    with_check
FROM pg_policies 
WHERE tablename = 'permissions'
ORDER BY policyname;

